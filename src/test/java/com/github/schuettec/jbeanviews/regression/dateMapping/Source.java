package com.github.schuettec.jbeanviews.regression.dateMapping;

import java.util.Date;

public class Source {

	private Date date;

	public Source(Date date) {
		super();
		this.date = date;
	}

	public Source() {
		super();
	}

	public Date getDate() {
		return date;
	}

	public void setDate(Date date) {
		this.date = date;
	}

}
